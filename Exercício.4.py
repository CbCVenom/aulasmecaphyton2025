def main():
    # Solicita ao usuário dois números inteiros
    num1 = int(input("Digite o primeiro número inteiro: "))
    num2 = int(input("Digite o segundo número inteiro: "))

    # Calcula a soma
    soma = num1 + num2

    # Exibe o resultado
    print(f"A soma de {num1} e {num2} é {soma}.")


# Executa o programa
if __name__ == "__main__":
    main()
